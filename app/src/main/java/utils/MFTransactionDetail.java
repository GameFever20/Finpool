public class MFTransactionDetail {


    String tradeDate, amount, purchase, units, sensex, navDate, valuation, divPay, divInvest, gain, cagr, span, abs;

    public MFTransactionDetail() {
    }


    public String getTradeDate() {
        return tradeDate;
    }

    public void setTradeDate(String tradeDate) {
        this.tradeDate = tradeDate;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getPurchase() {
        return purchase;
    }

    public void setPurchase(String purchase) {
        this.purchase = purchase;
    }

    public String getUnits() {
        return units;
    }

    public void setUnits(String units) {
        this.units = units;
    }

    public String getSensex() {
        return sensex;
    }

    public void setSensex(String sensex) {
        this.sensex = sensex;
    }

    public String getNavDate() {
        return navDate;
    }

    public void setNavDate(String navDate) {
        this.navDate = navDate;
    }

    public String getValuation() {
        return valuation;
    }

    public void setValuation(String valuation) {
        this.valuation = valuation;
    }

    public String getDivPay() {
        return divPay;
    }

    public void setDivPay(String divPay) {
        this.divPay = divPay;
    }

    public String getDivInvest() {
        return divInvest;
    }

    public void setDivInvest(String divInvest) {
        this.divInvest = divInvest;
    }

    public String getGain() {
        return gain;
    }

    public void setGain(String gain) {
        this.gain = gain;
    }

    public String getCagr() {
        return cagr;
    }

    public void setCagr(String cagr) {
        this.cagr = cagr;
    }

    public String getSpan() {
        return span;
    }

    public void setSpan(String span) {
        this.span = span;
    }

    public String getAbs() {
        return abs;
    }

    public void setAbs(String abs) {
        this.abs = abs;
    }
}
